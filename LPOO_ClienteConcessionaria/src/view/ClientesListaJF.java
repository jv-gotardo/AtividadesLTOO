    public void carregarClientes(){
        DefaultListModel modeloLista = new DefaultListModel();
        modeloLista.removeAllElements();
        for(Cliente cliente: clientes){
            modeloLista.addElement(cliente);
        }
        
        listaClientes.setModel(modeloLista);        
    }

