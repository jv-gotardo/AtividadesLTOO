    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        CadastroClienteJF cadastro = new CadastroClienteJF(this, true);
        cadastro.setVisible(true);
        
        Cliente itemCadastrado = cadastro.getCliente();
        clientes.add(itemCadastrado);
        
        carregarClientes();
    }//GEN-LAST:event_btnAdicionarActionPerformed
    
    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Cliente clienteSelecionado = listaClientes.getSelectedValue();
     
        if(clienteSelecionado != null){
            
            CadastroClienteJF cadastro = new CadastroClienteJF(this, true);
            cadastro.setCliente(clienteSelecionado);
            cadastro.setVisible(true);
            
            carregarClientes();
            
        } else {
            JOptionPane.showMessageDialog(rootPane, "Nenhum cliente selecionado", 
                    "Para realizar essa ação, selecione um cliente", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        Cliente clienteSelecionado = listaClientes.getSelectedValue();
        if(clienteSelecionado  != null){
            
            int remover = JOptionPane.showConfirmDialog(rootPane, 
                    "Remover cliente "+clienteSelecionado.getNome()+"?");
            
            if(remover == JOptionPane.YES_OPTION){
                clientes.remove(clienteSelecionado);
                carregarClientes();
            }
            
            
        } else {
            JOptionPane.showMessageDialog(rootPane, "Nenhum cliente selecionado", 
                    "Para realizar essa ação, selecione um cliente", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnLerDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLerDadosActionPerformed
        Cliente clienteSelecionado = listaClientes.getSelectedValue();
        if(clienteSelecionado != null){
            JOptionPane.showMessageDialog(rootPane, clienteSelecionado.toString());
        } else {
            JOptionPane.showMessageDialog(rootPane, "Nenhum cliente selecionado", 
                    "Para realizar essa ação, selecione um cliente", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLerDadosActionPerformed

