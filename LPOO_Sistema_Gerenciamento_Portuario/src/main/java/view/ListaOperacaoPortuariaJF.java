    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        // 1. Abrir a aplicação CadastroVendaJD
        CadastroOperacaoPortuariaJD telaOperacao = new CadastroOperacaoPortuariaJD(this, rootPaneCheckingEnabled);
        telaOperacao.setVisible(true);

        // 2. recuperar o objeto Venda
        OperacaoPortuaria novoObj = telaOperacao.getOperacao();

        // 3. Se o objeto não for null persistir no BD
        if (novoObj != null) {
            try {
                dao.persist(novoObj);
                loadOperacoes();
            } catch (Exception ex) {
                System.err.println("Erro ao salvar nova operação: " + novoObj + "\n Erro: " + ex);
            }

        }
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tblOperacoes.getSelectedRow() != -1) {
            int row = tblOperacoes.getSelectedRow();
            Long id = (Long) tblOperacoes.getValueAt(row, 0);  
            Optional<OperacaoPortuaria> optOperacao = dao.buscarPorId(id); 

            CadastroOperacaoPortuariaJD telaEdicao = new CadastroOperacaoPortuariaJD(this, rootPaneCheckingEnabled);
            telaEdicao.setOperacao(optOperacao.get());
            telaEdicao.setVisible(true);

                try {
                    dao.persist(telaEdicao.getOperacao());
                    JOptionPane.showMessageDialog(rootPane, "Operação atualizada com sucesso!");
                } catch (Exception ex) {
                    System.err.println("Erro ao editar operação\nErro: " + ex);
                    JOptionPane.showMessageDialog(rootPane, "Erro ao salvar alterações da operação.");
                }

                loadOperacoes();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Selecione uma operação");
            }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        if (tblOperacoes.getSelectedRow() != -1) {
            int row = tblOperacoes.getSelectedRow();
            Long id = (Long) tblOperacoes.getValueAt(row, 0);  
            Optional<OperacaoPortuaria> optOperacao = dao.buscarPorId(id); 

            if (optOperacao.isPresent()) {
                OperacaoPortuaria op = optOperacao.get();
                int oper = JOptionPane.showConfirmDialog(rootPane, "Deseja realmente remover esta operação?");
                if (oper == JOptionPane.YES_OPTION) {
                    try {
                        dao.remover(op);
                        JOptionPane.showMessageDialog(rootPane, "Operação removida com sucesso!");
                        loadOperacoes();
                    } catch (Exception ex) {
                        System.err.println("Erro ao remover Operação\nErro: " + ex);
                        JOptionPane.showMessageDialog(rootPane, "Erro ao remover Operação.");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Operação não encontrada.");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma Operação para remover.");
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnInformacoesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInformacoesActionPerformed
        if (tblOperacoes.getSelectedRow() != -1) {
            int row = tblOperacoes.getSelectedRow();
            Long id = (Long) tblOperacoes.getValueAt(row, 0);  
            Optional<OperacaoPortuaria> optOperacao = dao.buscarPorId(id); 
   
            if (optOperacao.isPresent()) {
                OperacaoPortuaria o = optOperacao.get();
                String info = String.format("""
                ID: %d
                Navio: %s
                Carga: R$ %s
                Berço: %d
                Funcionário: %s
                Tipo: %s
            """,
                o.getId(),
                o.getNavio().getNome(),
                o.getCarga().getDescricao(),
                o.getBerco().getNumero(),
                o.getFuncionario().getNome(),
                o.getTipo()
                );
                JOptionPane.showMessageDialog(rootPane, info);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Venda não encontrada.");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma venda para ver as informações.");
        }
    }//GEN-LAST:event_btnInformacoesActionPerformed

