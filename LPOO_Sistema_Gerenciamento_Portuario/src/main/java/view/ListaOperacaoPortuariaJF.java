    public void loadOperacoes(){
        DefaultTableModel modelo = (DefaultTableModel) tblOperacoes.getModel();
        modelo.setNumRows(0);
        for (OperacaoPortuaria obj : dao.listarTodas()) {
            Object[] linha = {
                obj.getId(),
                obj.getNavio(),
                obj.getCarga(),
                obj.getBerco(),
                obj.getFuncionario(),
                obj.getTipo(),
                obj.getDataHora().format(DateTimeFormatter.ISO_DATE),
            };
            modelo.addRow(linha);
        }
    }

